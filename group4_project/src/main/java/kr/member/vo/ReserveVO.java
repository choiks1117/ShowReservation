package kr.member.vo;

public class ReserveVO {
	private int re_key;
	private int me_num;
	private int sh_key;
	private String re_reserve;
	private String re_date;
	private String re_spon;
	public int getRe_key() {
		return re_key;
	}
	public void setRe_key(int re_key) {
		this.re_key = re_key;
	}
	public int getMe_num() {
		return me_num;
	}
	public void setMe_num(int me_num) {
		this.me_num = me_num;
	}
	public int getSh_key() {
		return sh_key;
	}
	public void setSh_key(int sh_key) {
		this.sh_key = sh_key;
	}
	public String getRe_reserve() {
		return re_reserve;
	}
	public void setRe_reserve(String re_reserve) {
		this.re_reserve = re_reserve;
	}
	public String getRe_date() {
		return re_date;
	}
	public void setRe_date(String re_date) {
		this.re_date = re_date;
	}
	public String getRe_spon() {
		return re_spon;
	}
	public void setRe_spon(String re_spon) {
		this.re_spon = re_spon;
	}
	
	
}
