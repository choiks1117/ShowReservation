package kr.community.vo;

public class CommunityFavVO {
	private int fav_key;
	private int me_key;
	private int co_key;
	
	public int getFav_key() {
		return fav_key;
	}
	public void setFav_key(int fav_key) {
		this.fav_key = fav_key;
	}
	public int getMe_key() {
		return me_key;
	}
	public void setMe_key(int me_key) {
		this.me_key = me_key;
	}
	public int getCo_key() {
		return co_key;
	}
	public void setCo_key(int co_key) {
		this.co_key = co_key;
	}
}
